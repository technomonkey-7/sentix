def t(key: str, lang: str | None = None, **kwargs) -> str:
    """Translation lookup. Reads the active language from
    st.session_state['lang'] when ``lang`` isn't given explicitly. Falls back
    to the raw key (so a missing translation never crashes the page)."""
    if lang is None:
        try:
            import streamlit as st
            lang = st.session_state.get("lang", "TR")
        except Exception:
            lang = "TR"
    table = T.get(lang, T["TR"])
    text = table.get(key) or T["TR"].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text
